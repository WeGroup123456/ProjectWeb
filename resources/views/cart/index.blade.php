    <script type="text/javascript">
    jQuery(function($) {

      $(".SoLuong").change(function(){
        var rowId = $(this).attr('id');
        var qty = $(this).val();
        var token = $("input[name='_token']").val();
        $.ajax({
          url:'ajax/update/'+rowId+'/'+qty,
          type:'GET',
          cache: false,
          data:{
            "_token":token,
            "id":rowId,
            "qty":qty
          },
          success: function(data){
            if (data == "oke"){
              window.location = "cart";
            }else{
              alert("No");
            }
          },error: function(){
            alert("error");
          }
        });
      });
      
    });
    </script>
